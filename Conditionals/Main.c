#include<stdio.h>

int main() 
{
	int day = 1;
	switch (day) 
	{
		case 1:
			printf("It is a Sunday.");
			break;
		case 7:
			printf("It is a Saturday.");
			break;
		default:
			printf("I can't wait for the weekend.");
	}
	//Output: "I can't wait for the weekend."                                

}




